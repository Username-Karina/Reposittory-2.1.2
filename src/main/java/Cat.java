public class Cat {

    private String name;
    private String species;
    private byte age;

    public String getName(){
        return name;
    }
    public void setName(String name){
        this.name = name;
    }
    public  String getSpecies(){
        return species;
    }
    public void setSpecies(String species){
        this.species = species;
    }
    public byte getAge(){
        return age;
    }
    public void setAge(byte age){
        this.age = age;
    }

}
